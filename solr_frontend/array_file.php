METHODOLOGY	
Local Government Area Proï¬le (IDP)
An assessment is conducted with key informants at the LGA 
level. The type of information collected at this level includes: 
displaced population estimates (households and individuals), 
time of arrival of IDPs, location of origin, reasons of displace-
ment and type of displacement locations. The assessment also 
captures whether IDPs originated from the LGA and records 
contacts of key informants and organizations assisting IDPs in 
the LGA. The main outcome of this assessment is the list of 
wards where IDPs are present. This list is used as a reference to 
continue the assessment at ward level.  
Local Government Area Proï¬le (Returnees)
An assessment is conducted with key informants at the LGA 
level. The type of information collected at this level includes: 
returnee population estimates (households and individuals), 
time of return, location of origin and initial reasons of displace-
ment. The main outcome of this assessment is the list of wards 
where are present. This list is used as a reference to continue 
the assessment at ward level.  
Ward level Proï¬le (IDP)
Assessments are conducted at ward level. The type of informa-
tion collected at this level includes: displaced population 
estimates (households and individuals), time of arrival of IDPs, 
location of origin, reasons of displacement and type of 
displacement locations. The assessment also includes informa-
tion on displacement originating from the ward, as well as a 
demographic calculator based on a sample of IDPs in host 
communities and camp-like settings.  The results for the wards 
are used to verify the information collected at LGA level. The 
ward assessment is carried out in all wards identifed as having 
IDP populations in the LGA list.	
Ward level Proï¬le (returnee)
Assessments are conducted at ward level. The type of informa-
tion collected at this level includes: returnee population 
estimates (households and individuals), time of return, 
location of origin and reasons of initial displacement. The 
results for the wards are used to verify the information collect-
ed through at LGA level. The ward assessment is carried out in 
all wards identifed as having returnee populations in the LGA 
list.
 Site assessments: 
The site assessments are undertaken in identifed IDP locations 
(camps, camp-like settings and host   communities) to capture 
detailed information on the key services available. Site assess-
ment forms are used to record the exact location and name of 
a site, accessibility constraints, size and type of the site, wheth-
er registration is available, and if natural hazards put the site at 
risk. The form also captures details about the IDP population, 
including their place of origin, and demographic information 
on the number of households with a breakdown by age and 
sex, as well as information on IDPs with specifc vulnerabilities. 
Furthermore, the form captures details on key access to 
services in diferent sectors: shelter and NFI, WASH, food, nutri-
tion, health, education, livelihood, communication, and 
protection. The information is captured through interviews 
with representatives of the site and other key informants, 
including IDP representatives.	
